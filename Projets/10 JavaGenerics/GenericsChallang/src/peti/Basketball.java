package peti;

public class Basketball extends Team{
    public Basketball(String name) {
        super(name);
    }

    @Override
    public int getWon() {
        return super.getWon();
    }

    @Override
    public void setWon(int won) {
        super.setWon(won);
    }

    @Override
    public int getLost() {
        return super.getLost();
    }

    @Override
    public void setLost(int lost) {
        super.setLost(lost);
    }

    @Override
    public int getTied() {
        return super.getTied();
    }

    @Override
    public void setTied(int tied) {
        super.setTied(tied);
    }

    @Override
    public String getName() {
        return super.getName();
    }
}
