package lectureCodes.week03.classesObjects1.oneStepAtATime.iteration1instanceVariables;

/**
 * Rectangle encapsulates or represents a real-world rectangle.
 * It's a type. We need to create variables of this type, that
 * are called instances or objects
 * 
 */
public class Rectangle {
	/**
	 * every Rectangle, at its core, has two values,
	 * width and height (or length and breadth)
	 * these are called "instance variables", since there
	 * is a set of them for each instance
	 */
	public double width, height;
}
